  <h2><span id="Types_of_donor">Types of donor</span></h2>
  <p>Organ donors may be living, or brain dead. Brain dead means the donor must have received an injury (either traumatic or pathological) to the part of the brain that controls heartbeat and breathing. Breathing is maintained via artificial sources, which, in turn, maintains heartbeat. Once brain death has been declared the person can be considered for organ donation. Criteria for brain death vary. Because less than 3% of all deaths in the U.S. are the result of brain death, the overwhelming majority of deaths are ineligible for organ donation, resulting in severe shortages. Tissue may be recovered from donors who are cardiac dead. That is, their breathing and heartbeat has ceased. They are referred to as cadaveric donors. In general, tissues may be recovered from donors up to 24 hours past the cessation of heartbeat. In contrast to organs, most tissues (with the exception of corneas) can be preserved and stored for up to five years, meaning they can be &quot;banked.&quot; Also, more than 60 grafts may be obtained from a single tissue donor. Because of these three factors, the ability to recover from a non-heart beating donor, the ability to bank tissue, and the number of grafts available from each donor, tissue transplants are much more common than organ transplants. The American Association of Tissue Banks estimates that more than one million tissue transplants take place in the United States each year.</p>
  <dl>
    <dt><strong>Living</strong></dt>
  </dl>
  <p>In &quot;living donors&quot;, the donor remains alive and donates a renewable tissue, cell (e.g. skin), or donates an organ or part of an organ in which the remaining organ can regenerate or take on the workload of the rest of the organ (primarily single kidney donation, partial donation of liver, small bowel).&nbsp;<a href="http://en.wikipedia.org/wiki/Regenerative_medicine" title="Regenerative medicine">Regenerative medicine</a>&nbsp;may one day allow for laboratory-grown organs, using patient's own cells via stem cells, or healthy cells extracted from the failing organs.</p>
  <dl>
    <dt><strong>Deceased</strong></dt>
  </dl>
  <p>Deceased (formerly cadaveric) are donors who have been declared&nbsp;<a href="http://en.wikipedia.org/wiki/Brain_death" title="Brain death">brain-dead</a>&nbsp;and whose organs are kept viable by ventilators or other mechanical mechanisms until they can be excised for transplantation. Apart from brain-stem dead donors, who have formed the majority of deceased donors for the last twenty years, there is increasing use of Donation after Cardiac Death Donors (formerly non-heart beating donors) to increase the potential pool of donors as demand for transplants continues to grow.<span title="This claim needs references to reliable sources from October 2009">citation needed</span></a></em>]&nbsp;These organs have inferior outcomes to organs from a brain-dead donor; however given the scarcity of suitable organs and the number of people who die waiting, any potentially suitable organ must be considered.</p>
